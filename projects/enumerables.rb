require "byebug"
class Array

    def my_each(&prc)
        i = 0
        while i < self.length
            prc.call(self[i])
            i += 1
        end
        self
    end

    def my_select(&prc)
        arr = []
        self.my_each do |ele|
            arr << ele if prc.call(ele)
        end
        return arr
    end

    def my_reject(&prc)       
        arr =[]
        self.my_each { |ele| arr << ele unless prc.call(ele)}
        arr
    end

    def my_any?(&prc)
        self.my_each { |ele| return true if prc.call(ele) }
        false
    end

    def my_all?(&prc)
        self.my_each { |ele| return false unless prc.call(ele)}
        true
    end

    def my_flatten 
        arr = []
        self.each do |ele|
            if ele.is_a?(Array)
                arr += ele.my_flatten
            else
                arr << ele
            end 
        end
        arr
    end

    def my_zip(*arrays)
        zip = []

        (0...self.length).each do |i|
            subZip = [self[i]]
            arrays.my_each do |array|
                subZip << array[i]
            end
            zip << subZip
        end
        zip
    end

    def my_rotate(num=1)
        i = num % self.length
        
        i.times {self.push(self.shift) }
        self
        
    end
   
end

